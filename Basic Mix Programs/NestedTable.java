class NestedTable
{
	public static void main(String arg[])
	{
		int b=2;
		int num=1;
		while(num<=10)
		{
		  int a=1;
		  while(a<=10)
			{
			  System.out.printf(a+"*"+b+" = "+(a*b));
			  System.out.println(" ");
			  a++;
			 }
			 System.out.println();
			 b++;
			 num++;
		 }
		
		
	}
	
	
}



