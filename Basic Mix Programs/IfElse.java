class IfElse 
{
	public static void main(String args[]){
	int a=12;
	if(a>12)
	{
	  System.out.println("a is greater then 12");
	 }
	else if(a<12)
	{
	  System.out.println("a is less then 12");
	 }
	else if(a>15)
	{
	  System.out.println("a is greater then 15");  
	 }
	else
	{
	  System.out.println("invalid");
	 }
	}

}